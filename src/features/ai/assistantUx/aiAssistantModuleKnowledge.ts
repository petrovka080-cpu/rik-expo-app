export function getAiAssistantContextPrimer(context: AssistantContext): string | null {
  switch (context) {
    case "buyer":
    case "market":
    case "supplierMap":
    case "request":
      return "Вы открыли контекст снабжения.";
    case "warehouse":
      return "Вы открыли складской контекст.";
    case "accountant":
      return "Вы открыли финансовый контекст.";
    case "director":
    case "reports":
      return "Вы открыли директорский контекст.";
    case "foreman":
      return "Вы открыли контекст прораба.";
    case "contractor":
      return "Вы открыли контекст подрядчика.";
    default:
      return null;
  }
}
