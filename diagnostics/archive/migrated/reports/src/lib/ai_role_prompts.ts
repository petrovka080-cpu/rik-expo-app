type RoleType = 'buyer' | 'foreman' | 'warehouse' | 'accountant' | 'director' | 'construction_agent';

const ROLE_PROMPTS: Record<string, string> = {
  buyer: BUYER_PROMPT,
  foreman: FOREMAN_PROMPT,
  warehouse: WAREHOUSE_PROMPT,
  accountant: ACCOUNTANT_PROMPT,
  director: DIRECTOR_PROMPT,
  construction_agent: CONSTRUCTION_AGENT_PROMPT,
};

/**
 * Get the system prompt for a specific role.
 * Falls back to director prompt if role is unknown.
 */
export const getRoleSystemPrompt = (role?: string, companyId?: string | null): string => {
  const basePrompt = ROLE_PROMPTS[role || 'director'] || DIRECTOR_PROMPT;

  if (companyId) {
    return `${basePrompt}\n\nКонтекст: company_id = ${companyId}`;
  }

  return basePrompt;
};

/**
 * Get the list of available actions for a role.
 */
export const getRoleActions = (role: string): string[] => {
  switch (role) {
    case 'buyer':
      return ['search_supplier', 'set_price', 'approve_items', 'compare_prices', 'search_market', 'search_market_wide', 'chat'];
    case 'foreman':
      return ['parse_items', 'create_request', 'calculate_materials', 'search_market', 'search_market_wide', 'clarify', 'chat'];
    case 'warehouse':
      return ['accept_delivery', 'check_stock', 'track_usage', 'find_deficit', 'chat'];
    case 'accountant':
      return ['check_invoices', 'mark_paid', 'calculate_debt', 'payment_report', 'chat'];
    case 'construction_agent':
      return ['generate_estimate', 'calculate_materials', 'analyze_blueprint', 'analyze_photo', 'chat'];
    default:
      return ['search_market', 'search_market_wide', 'parse_items', 'chat'];
  }
};

export { CONSTRUCTION_AGENT_PROMPT };
export default { getRoleSystemPrompt, getRoleActions };
